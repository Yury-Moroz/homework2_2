export class Calculator {
  sum(a: number, b: number) {
    return a + b;
  }
  sub(a: number, b: number) {
    return a - b;
  }
  dev(a: number, b: number) {
    return a / b;
  }
  mult(a: number, b: number) {
    return a * b;
  }
  compMult(a: number, b: number, c: number) {
    return a * b * c;
  }
}
